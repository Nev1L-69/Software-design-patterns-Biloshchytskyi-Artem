class Light {
    public void turnOn() {
        System.out.println("The lights are turned on.");
    }

    public void turnOff() {
        System.out.println("The lights are turned off.");
    }
}

class Thermostat {
    public void setTemperature(int temperature) {
        System.out.println("The thermostat is set to " + temperature + " degrees.");
    }
}

class DoorLock {
    public void lock() {
        System.out.println("The doors are locked.");
    }

    public void unlock() {
        System.out.println("The doors are unlocked.");
    }
}
